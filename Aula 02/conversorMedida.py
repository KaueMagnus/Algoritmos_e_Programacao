'''Peça ao usuário uma distância em metros.
Converta essa distância para centímetros e milímetros. 
Apresente ambos os valores convertidos.'''

distancia = float(input("Coloque a distância"))
cent = distancia * 100
mm = distancia * 1000
print (f"A distância em centímetros é: {cent} e em milímitros é: {mm}")

