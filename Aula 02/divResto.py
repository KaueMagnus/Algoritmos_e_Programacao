'''Solicite dois números ao usuário.
Calcule o resultado da divisão e o resto. Apresente ambos os valores.
'''

num1 = float(input("Coloque um número"))
num2 = float(input("Coloque o segundo número"))
div = num1 / num2
div2 = num1 % num2
print (f"O resultado da divisão é:{div} e o resultado do resto é: {div2}")