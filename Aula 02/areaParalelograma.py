'''Área de um Paralelogramo
   Solicite ao usuário a base e a altura de um paralelogramo.
   Calcule a área. Mostre a área calculada.
'''

base = float(input("Digite o valor da base do paralelograma (metros): "))
altura = float(input("Digite o valor da altura do paralelograma (metros): "))

area = float(base * altura)

print(f"A area do paralelograma é de: {area} metros quadrados")

