'''17. Área de um Triângulo
   Solicite ao usuário a base e a altura de um triângulo. 
   Calcule a área com Área = (base * altura) / 2. Mostre a área calculada.
'''

base = float(input("Digite o valor da base do triangulo:"))
altura = float(input("Digite o valor da altura do triangulo:"))
area = float((base * altura) / 2)

print(f"A área do triangulo é {area}")